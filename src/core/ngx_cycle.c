ngx_uint_t             ngx_test_config;
//nginx -T参数 标识
ngx_uint_t             ngx_dump_config;
//nginx -q参数 标识, 该参数用于在启动时不输出任何日志信息，除非发生错误
ngx_uint_t             ngx_quiet_mode;


/* STUB NAME */
static ngx_connection_t  dumb;
